public class Day3Pyramids {

    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("#"); // print #
            }
            System.out.println(""); //print a blank
        }
    }

}